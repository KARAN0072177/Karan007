let day = 2;
let dayName;

switch(day) {
    case 1:
        dayName = "Sunday";
        break;
    case 2:
        dayName = "Monday";
        break;
    case 3:
        dayName = "Tuesday";
        break;
    default:
        dayName = "Unknown day";
}

console.log(dayName);  // Output: Monday